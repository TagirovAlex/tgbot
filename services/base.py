"""
Базовый класс сервиса.

Этот модуль содержит абстрактный базовый класс для всех сервисов приложения.
Сервисы инкапсулируют бизнес-логику и предоставляют высокоуровневый интерфейс
для работы с данными.
"""

from abc import ABC
from database.database import Database


class BaseService(ABC):
    """
    Абстрактный базовый класс для сервисов.
    
    Сервисы реализуют бизнес-логику приложения, работая с репозиториями
    и другими сервисами. Они предоставляют высокоуровневый API для
    обработчиков бота.
    
    Attributes:
        db: Экземпляр Database для работы с базой данных
    
    Example:
        >>> class MyService(BaseService):
        ...     '''Мой кастомный сервис.'''
        ...     
        ...     async def do_something(self, data: dict) -> bool:
        ...         '''Выполнить какое-то действие.'''
        ...         # Бизнес-логика
        ...         return True
    
    Note:
        Все методы сервиса должны быть асинхронными для совместимости
        с асинхронной архитектурой бота.
    """
    
    def __init__(self, db: Database):
        """
        Инициализация сервиса.
        
        Args:
            db: Экземпляр Database для работы с базой данных
        """
        self.db = db